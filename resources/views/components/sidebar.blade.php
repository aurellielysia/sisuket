    document.addEventListener('DOMContentLoaded', function () {
        const toggleButton = document.getElementById('data-ktp-toggle');
        const dropdownMenu = document.getElementById('data-ktp-dropdown');

        toggleButton.addEventListener('click', function (event) {
            event.preventDefault(); // Mencegah aksi default dari link
            dropdownMenu.classList.toggle('hidden'); // Menyembunyikan atau menampilkan dropdown
        });

        // Optional: Menyembunyikan dropdown jika klik di luar dropdown
        document.addEventListener('click', function (event) {
            if (!toggleButton.contains(event.target) && !dropdownMenu.contains(event.target)) {
                dropdownMenu.classList.add('hidden');
            }
        });
    });
</script>

